# 8) 리턴값이 있는 멤버함수(메소드)도 만들어 사용가능. 
# [파이썬 , 웹, 인공지능 교과목 점수를 한번에 저장하는 클래스 설계]
class Stu:
    def __init__(self,name='익명',python=0,web=0,ai=0): # 파라미터의 default 값으로 호출할때 파라미터 값을 안주어도 가능.
        self.name = name
        self.python = python 
        self.web = web
        self.ai = ai 

    def show(self):
        print(self.name, self.python ,self.web, self.ai)
        print()


    #총점을 계산하여 리턴해 주느 기능
    def get_total(self):
        return self.python + self.web + self.ai
    
# Stu class 설계 끝

# 객체 생성.
s1= Stu('sam',80,70,60)
s2= Stu('robin',60,50,50)


s1.show()
s2.show()


# 각 학생들의 총점을 출력하고 싶다면 ??
# 메소드로 부터 retun 된것을 사용하는 모슴. 
total1=s1.get_total()
print('첫번째 학생의 총점:',total1)

total2=s2.get_total()
print('두번째 학생의 총점:',total2)

