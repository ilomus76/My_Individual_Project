# 클래스 설계 및 생성자함수에 값 전달까지...
class Person:
    def __init__(self,name, age, address):
        self.name = name # 내 이름에 이름을 전달받을 것이다.
        self.age = age
        self.address = address
        print('Person 객체를 생성했습니다.')

    def show(self):
        print('이름:',self.name)
        print('나이:',self.age)
        print('주소:',self.address)
        print()

# -------------- Person 설계도면 끝 ...

# 객체생성
#p1= Person() # __init__ 함수에 파라미터가 필요해서 이러면 error
p1= Person('sam',20,'seoul') #
p1.show()

p2=Person('robin',25,'busan')
p2.show() 


